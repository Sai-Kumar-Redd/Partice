public class Application3 {
    public static void main(String[]args){
         for(int i=1;i<=3;i++){         //Nested LOOP  means inside the loop other loop will be that is
                                           // called Nested LOOP.
             for(int j=1;j<=3;j++){
                 System.out.println(i +"  "+j);
             }
         }
    }
}
